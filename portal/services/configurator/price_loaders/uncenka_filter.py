"""Регекс-фильтр SKU-уценок и поврежденных позиций при загрузке прайсов.

Цель: не попадать в каталог printers_mfu (и ПК-таблицы тоже) позиции,
которые дистрибьютор продаёт с уценкой / повреждённой упаковкой /
восстановленные / б/у — для аукционов это нерелевантно (нельзя
заявить новый товар с уценкой по 44-ФЗ; для конфигуратора такие
SKU тоже создают шум, потому что цена и наличие у уценки разовые).

Применяется в `app/services/price_loaders/orchestrator.py::_process_row`
ДО создания скелета и записи `supplier_prices`. Чистый regex,
без вызовов БД — пригоден для unit-тестов и для скрипта-догонялки
`scripts/cleanup_uncenka_skus.py`, который чистит уже накопленный
каталог по тем же паттернам.

Решение мини-этапа 9a-uncenka (2026-05-10): паттерны определены
прицельно под обороты, реально встречающиеся в прайсах поставщиков,
а не на всякий маркер слова «уценённый» в свободном тексте. False-
positive-риск зафиксирован в стоп-условиях: если живой smoke
покажет агрессивную чистку (FP > порога) — паттерны сужаются.
"""

from __future__ import annotations

import re

# Паттерны маркеров уценки/повреждения/восстановления.
# IGNORECASE+UNICODE учтены через флаги при компиляции.
#
# Порядок: от наиболее специфичных к универсальным. Каждый паттерн
# покрывает один класс позиций, дублирование намеренное — повышает
# читаемость и упрощает локальную правку (если выяснится, что
# конкретный паттерн ловит FP, его можно сузить точечно).
UNCENKA_PATTERNS: list[str] = [
    # Уценка/уценённый/уценённая — основной триггер.
    # «уценка», «уценку», «уценкой», «уценённый», «уценённая», «уцененный».
    r"уцен(к|ённ|енн)",

    # Повреждение/повреждённый/повреждена — комплект форм слова «повредить».
    # Покрывает «повреждение коробки», «упаковка повреждена», «поврежденный»,
    # «повреждённая упаковка» и т. п. без специальных под-паттернов.
    r"повре[жз]д[еёа]н",

    # Сокращение «повр.» рядом с «коробк»/«упаковк» — пишут поставщики
    # в формате «G&G P2022W (повр. коробк.)».
    r"повр\.?\s*(коробк|упаковк)",

    # Б/У — варианты разделителя: «б/у», «б\\у», «б-у», «б у», «бу».
    # Слово «бу» в литературном русском редкое, поэтому даём его как
    # отдельный токен с границами слова — FP минимален.
    r"\bб\s*[/\\\-]?\s*у\b",
    r"\bбу\b",

    # Refurbished / reconditioned / восстановленный.
    r"refurb",
    r"recond",
    r"восстановл",
    r"восстано?вле?нн",
    r"восстан\.",

    # Витринный / выставочный образец.
    r"выставочн",
    r"витринн",

    # Open box и его сокращение OS — английские маркеры, встречаются
    # в импортных прайсах. \bos\b ограничен латиницей: кириллическая
    # «ОС» (операционная система) — другой набор кодпоинтов и под
    # \b...\b не попадает.
    r"open\s*box",
    r"\bos\b",
]

# Один скомпилированный паттерн — модулю нужен один проход по строке.
_UNCENKA_RE = re.compile(
    "|".join(f"(?:{p})" for p in UNCENKA_PATTERNS),
    flags=re.IGNORECASE | re.UNICODE,
)


def is_uncenka(name: str | None) -> bool:
    """True если имя позиции содержит маркер уценки/повреждения/б-у/refurb.

    None и пустая строка трактуются как «нет маркера» — orchestrator не
    должен отбрасывать строку только за отсутствие name (там другие
    инварианты).
    """
    if not name:
        return False
    return _UNCENKA_RE.search(name) is not None

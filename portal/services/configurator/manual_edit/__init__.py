# Модуль ручного редактирования компонентов (этап 2.5В).
#
# Содержит:
#   - schema.py         — список редактируемых полей по категориям, CSV-формат;
#   - validators_extra.py — валидаторы для обязательных и опциональных полей;
#   - csv_io.py         — сериализация/десериализация ячеек CSV;
#   - persistence.py    — запись значений с source='manual' (может перезаписывать);
#   - exporter.py       — выгрузка компонентов в CSV;
#   - importer.py       — применение правок из CSV к БД;
#   - editor.py         — операции для точечных правок (show / update / add / delete).

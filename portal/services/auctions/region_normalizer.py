# Канонизация названий регионов для матчинга карточек zakupki со
# стоп-листом `excluded_regions`.
#
# На zakupki «Место нахождения» приходит в разных формах:
#   «Магаданская обл», «Магаданская область», «МОСКОВСКАЯ ОБЛАСТЬ»,
#   «Саха (Якутия) Респ», «Чеченская Респ», «Татарстан Респ», «г.Москва».
# Seed-список `excluded_regions` хранит «Магаданская область»,
# «Якутия», «Чукотский АО» и т.д. Точное строковое равенство в
# `filters.py::compute_flags` не срабатывает — лоты из стоп-регионов
# не получают `flags_jsonb.excluded_by_region=true` и проходят в инбокс.
#
# Канонизация применяется С ОБЕИХ СТОРОН (и к seed, и к customer_region
# из карточки) — функция должна быть идемпотентной для уже канонических
# форм и устойчивой к регистру/сокращениям.

from __future__ import annotations

import re


# Префикс «г.», «г », «город » — но не «Гранитный» (без точки/пробела
# «г» — часть другого слова). Точка с пробелом необязательна,
# пробел после «г» без точки обязателен.
_PREFIX_GORODA_RE = re.compile(r"^(?:г\.\s*|г\s+|город\s+)", re.IGNORECASE)
# `\b` после `\.?` не работает, когда дальше пробел: между '.' и ' ' нет
# word-boundary (оба — non-word). Поэтому опциональную точку выносим из
# хвостового \b и обрабатываем отдельно.
_OBL_RE = re.compile(r"\bобл\b\.?")
_RESP_RE = re.compile(r"\bресп\b\.?")
_MULTISPACE_RE = re.compile(r"\s+")


def canonical_region(name: str | None) -> str:
    """Каноническая (lowercase) форма названия региона.

    Возвращает пустую строку для None/пустого входа. Идемпотентна:
    `canonical_region(canonical_region(x)) == canonical_region(x)`.

    Правила:
    - lowercase + trim + collapse whitespace;
    - префикс «г.» / «город» отбрасывается;
    - «Якутия» = «Саха» = «Саха (Якутия)» → "якутия" (исторический синоним);
    - «обл» / «обл.» → «область»;
    - «респ» / «респ.» → «республика»;
    - если в строке остаётся «республика» — оно выносится в начало:
      «татарстан республика» → «республика татарстан». Это нормализует
      разные порядки слов с zakupki («Татарстан Респ» vs «Республика Татарстан»).
    """
    if not name:
        return ""
    s = _MULTISPACE_RE.sub(" ", name).strip().lower()
    if not s:
        return ""

    # Якутия и Саха — один и тот же регион, исторические названия.
    # Проверяем ДО снятия «г.»/сокращений, чтобы поймать «Саха (Якутия) Респ».
    if "якутия" in s or s == "саха" or s.startswith("саха ") or " саха" in s:
        return "якутия"

    s = _PREFIX_GORODA_RE.sub("", s)
    s = _OBL_RE.sub("область", s)
    s = _RESP_RE.sub("республика", s)

    if "республика" in s and not s.startswith("республика "):
        parts = [p for p in s.split() if p != "республика"]
        s = "республика " + " ".join(parts)

    return _MULTISPACE_RE.sub(" ", s).strip()

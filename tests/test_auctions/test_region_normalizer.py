"""Тесты канонизации названий регионов (фикс 2026-05-13).

Покрывают наблюдаемые на pre-prod формы customer_region:
- сокращение «обл» / «обл.» vs полное «область»
- сокращение «респ» / «респ.» vs полное «Республика»
- разный регистр («МОСКОВСКАЯ ОБЛАСТЬ»)
- разный порядок («Татарстан Респ» vs «Республика Татарстан»)
- исторический синоним «Саха (Якутия)» = «Якутия»
- префикс «г.» (для городов федерального значения)
- идемпотентность.
"""
from __future__ import annotations

import pytest

from portal.services.auctions.region_normalizer import canonical_region


@pytest.mark.parametrize(
    "raw,expected",
    [
        ("Магаданская область", "магаданская область"),
        ("Магаданская обл", "магаданская область"),
        ("Магаданская обл.", "магаданская область"),
        ("МАГАДАНСКАЯ ОБЛАСТЬ", "магаданская область"),

        ("Приморский край", "приморский край"),
        ("Камчатский край", "камчатский край"),

        ("Калининградская область", "калининградская область"),
        ("Калининградская обл", "калининградская область"),

        ("Чукотский АО", "чукотский ао"),
        ("ЧУКОТСКИЙ АО", "чукотский ао"),

        ("Якутия", "якутия"),
        ("Саха (Якутия) Респ", "якутия"),
        ("Республика Саха (Якутия)", "якутия"),
        ("Саха", "якутия"),

        ("Татарстан Респ", "республика татарстан"),
        ("Республика Татарстан", "республика татарстан"),
        ("Крым Респ", "республика крым"),
        ("Чеченская Респ", "республика чеченская"),

        ("Сахалинская область", "сахалинская область"),
        ("Сахалинская обл", "сахалинская область"),

        # Идемпотентность.
        ("магаданская область", "магаданская область"),
        ("якутия", "якутия"),

        # Префикс «г.» для городов федерального значения.
        ("г. Москва", "москва"),
        ("г.Москва", "москва"),
        ("Город Санкт-Петербург", "санкт-петербург"),

        # Граничные.
        ("", ""),
        ("   ", ""),
        ("  Магаданская   область  ", "магаданская область"),
    ],
)
def test_canonical_region_normalizes(raw, expected):
    assert canonical_region(raw) == expected


def test_canonical_region_none_returns_empty():
    assert canonical_region(None) == ""


def test_canonical_region_is_idempotent():
    """Двойное применение даёт тот же результат, что и одно."""
    for raw in (
        "Магаданская обл",
        "Саха (Якутия) Респ",
        "Татарстан Респ",
        "Чукотский АО",
        "МОСКОВСКАЯ ОБЛАСТЬ",
    ):
        once = canonical_region(raw)
        twice = canonical_region(once)
        assert once == twice, f"{raw!r}: {once!r} → {twice!r}"


def test_canonical_seed_matches_observed_card_form():
    """Канонические формы seed (excluded_regions) совпадают с тем,
    что приходит из card_parser._extract_region на pre-prod лотах.

    Это инвариант, ради которого вообще создан region_normalizer:
    seed «Магаданская область» и карточка «Магаданская обл» должны
    дать одинаковый канонический ключ.
    """
    seed_to_card = {
        "Магаданская область":     "Магаданская обл",
        "Приморский край":         "Приморский край",
        "Калининградская область": "Калининградская обл",
        "Сахалинская область":     "Сахалинская обл",
        "Якутия":                  "Саха (Якутия) Респ",
        "Камчатский край":         "Камчатский край",
        "Чукотский АО":            "Чукотский АО",
    }
    for seed, card in seed_to_card.items():
        assert canonical_region(seed) == canonical_region(card), (
            f"seed {seed!r} ({canonical_region(seed)!r}) ≠ "
            f"card {card!r} ({canonical_region(card)!r})"
        )

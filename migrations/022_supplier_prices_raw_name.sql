-- КВАДРО-ТЕХ: миграция 022 — оригинальное название позиции от
-- поставщика в supplier_prices (этап 11.4)
--
-- Что меняет:
--   Добавляет в supplier_prices колонку raw_name (TEXT, NULL).
--   Это «то, что поставщик написал в этой загрузке» — название
--   из строки прайса как есть. Обновляется при каждой загрузке,
--   даже если стало короче или беднее предыдущего: на момент
--   подбора у компонента всё равно есть имя (model) и имена от
--   других поставщиков, а агрегацию/regex-обогащение по raw_name
--   делает отдельный шаг enrichment (этап 11.6).
--
-- Зачем:
--   До 11.4 raw_name хранился ТОЛЬКО в unmapped_supplier_items —
--   а это таблица для строк, требующих ручной проверки. Для
--   нормально сматченных позиций мы оригинальное название от
--   поставщика теряли. С 11.4 хотим сохранять его всегда — и
--   для UI «Сравнение прайсов», и для будущего обогащения.
--
--   updated_at в supplier_prices уже существует с миграции 001;
--   отдельная колонка для него не нужна.
--
-- Идемпотентно: ADD COLUMN IF NOT EXISTS — повторный накат на
-- prod не падает.
-- =============================================================

ALTER TABLE supplier_prices
    ADD COLUMN IF NOT EXISTS raw_name TEXT;

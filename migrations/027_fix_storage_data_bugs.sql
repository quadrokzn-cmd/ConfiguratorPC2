-- (этап 11.6.2.7, финал AI-блока).
--
-- Что меняет:
--   Исправляет ошибочный interface='SAS' у storages.id=1059
--   (WD Red WDS100T1R0A — это SATA-SSD 2.5" NAS-серии WD Red SA500,
--   а не SAS-диск). Источник истины: westerndigital.com/products/
--   internal-drives/wd-red-sa500-nas-sata-ssd.
--
-- Зачем:
--   Хвост AI-обогащения 11.6.2.6.1b: после прохода по 35 storages
--   на интерфейс/форм-фактор/тип/ёмкость остался один single-row bug —
--   regex-нормализатор неправильно интерпретировал «WDS100T1R0A» как
--   SAS из-за совпадения по неудачному паттерну. Исправляем явным
--   UPDATE'ом, чтобы не расширять нормализатор для одиночного кейса.
--
-- Идемпотентно через AND interface='SAS' — повторный прогон
-- ничего не меняет.
-- =============================================================

UPDATE storages
   SET interface = 'SATA'
 WHERE id = 1059
   AND interface = 'SAS';

-- =============================================================
-- КВАДРО-ТЕХ: миграция 025 — переклассификация чужих в motherboards
-- (этап 11.6.2.6.0b)
--
-- Что меняет:
--   Скрывает 3 строки в motherboards, которые на самом деле не
--   являются материнскими платами:
--     id 794 → ASUS E5402WVAK (моноблок Asus AiO ExpertCenter E5);
--     id 805 → ASUS ESD-S1CL (внешний enclosure для SSD);
--     id 811 → ASUS ESD-S1C (внешний enclosure для SSD).
--
-- Зачем:
--   Аудит 6.0a (см. диагностику в _storage_audit.py) обнаружил эти
--   чужие позиции в motherboards. Их не нужно переносить в storages
--   или другие категории (моноблок не относится ни к одной из 8
--   таблиц компонентов; enclosures — это аксессуары, как рамки 2.5"→
--   3.5"). Достаточно скрыть, чтобы они не попадали в подбор плат.
--
-- Идемпотентно через AND is_hidden = FALSE: вторая попытка ничего не
-- делает.
--
-- Что НЕ меняется:
--   * supplier_prices не трогаем — связки price→component_id остаются
--     на motherboards.id (которые скрыты).
--   * INSERT в storages не делаем (enclosures — внешние накопители,
--     не подходят под схему storages, и сам ASUS E5402WVAK тем более).
-- =============================================================

UPDATE motherboards
   SET is_hidden = TRUE
 WHERE id IN (794, 805, 811)
   AND is_hidden = FALSE;

-- =============================================================
-- КВАДРО-ТЕХ: миграция 026 — скрытие Kingston SNA-BR2/35 рамки
-- (этап 11.6.2.6.0b, дополнение)
--
-- Что меняет:
--   Помечает is_hidden=TRUE для storages.id 782 — Kingston Brackets
--   and Screws 2.5"→3.5" (рамка-переходник для крепления SSD/HDD).
--
-- Зачем:
--   Аудит 6.0a обнаружил 2 рамки в storages: id=782 (Kingston SNA-
--   BR2/35) и id=1133 (Digma DGBRT2535). Скрипт reclassify_storage_
--   misclassified.py через детектор is_likely_non_storage поймал
--   только id=1133. У id=782 поле storage_type='SSD' заполнено
--   старым нормализатором ошибочно (рамка не SSD), и защитный слой
--   детектора по непустому storage_type его пропускает. Эту защиту
--   ослаблять нельзя (она нужна для большинства настоящих SSD/HDD),
--   поэтому id=782 закрываем явной миграцией.
--
-- Идемпотентно через AND is_hidden = FALSE.
--
-- Что НЕ меняется:
--   * supplier_prices не трогаем — связки остаются на storages.id 782.
-- =============================================================

UPDATE storages
   SET is_hidden = TRUE
 WHERE id = 782
   AND is_hidden = FALSE;

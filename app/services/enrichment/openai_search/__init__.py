# Модуль обогащения характеристик через OpenAI Web Search (этап 2.5В).
#
# Назначение: автоматически обогащать ТОЛЬКО новые SKU, которые появились
# при загрузке очередного прайса. Массовая добивка по оставшимся 975 NULL
# НЕ выполняется — это явное архитектурное решение.
#
# Состав:
#   - schema.py      — какие поля обогащаем, константы источников;
#   - skip_rules.py  — правила «правильных NULL» (NULL по правилу — это норма);
#   - fx.py          — актуальный курс USD/RUB из ЦБ РФ с суточным кэшем;
#   - cost_guard.py  — оценка стоимости и интерактивное подтверждение;
#   - client.py      — обёртка над openai.chat.completions с web_search_options;
#   - runner.py      — основной цикл: отбор → skip_rules → client → БД;
#   - hooks.py       — точка авто-запуска после price_loader;
#   - report.py      — форматирование итогового отчёта по прогону.

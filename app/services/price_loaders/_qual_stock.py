# Общая таблица качественных значений колонки «Склад» Treolan.
#
# Treolan и в XLSX, и в REST-API отдаёт остаток не только числом, но и
# буквенными маркерами: «много», «<10», «>10», «>100». Без перевода в
# числа конфигуратор (фильтр stock_qty > 0) такие позиции не увидит.
#
# Раньше таблица жила в app/services/price_loaders/treolan.py (XLSX-loader),
# а API-парсер app/services/auto_price/fetchers/treolan.py её игнорировал —
# отсюда баг 12.3-fix-2 (run #18 потерял ~700 «много»-позиций как stock=0).
# Теперь оба парсера импортируют ИМЕННО эту таблицу — рассинхронизация
# исключена by design.
#
# Контракт ключей: lower-case, без пробелов (значение нормализуется
# вызывающей стороной через `.strip().lower().replace(" ", "")` перед
# lookup'ом).

from __future__ import annotations


TREOLAN_QUAL_STOCK: dict[str, int] = {
    "<10":   5,
    "много": 50,
    ">10":   20,
    ">100":  100,
}
